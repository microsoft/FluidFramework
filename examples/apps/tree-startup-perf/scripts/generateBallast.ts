/**
 * Generates synthetic TypeScript modules under src/generated/ to serve as
 * realistic JavaScript ballast for bundle-size sweep testing.
 *
 * Each module contains classes with methods that perform non-trivial work
 * (hashing, Map manipulation, array transforms) so the code has realistic
 * parse, compile, and execution cost after minification.
 *
 * Usage (called by sweep.ts, but can also be run standalone):
 *
 * npx tsx scripts/generate-ballast.ts --chunks=20
 */

import { mkdirSync, rmSync, writeFileSync } from "node:fs";
import { join } from "node:path";
import { parseArgs } from "node:util";

const { values: args } = parseArgs({
	options: {
		chunks: { type: "string", default: "0" },
		outDir: { type: "string", default: "_generated" },
	},
});

const chunkCount = Number(args.chunks);
const outDir = args.outDir ?? join("src", "generated");

/**
 * Generate a single synthetic module (~4 KB of minifiable TypeScript).
 * Each module is unique via its index, preventing deduplication by bundlers.
 */
function generateChunk(index: number): string {
	const pad = String(index).padStart(4, "0");
	return `/*!
 * Copyright (c) Microsoft Corporation and contributors. All rights reserved.
 * Licensed under the MIT License.
 */

/* eslint-disable @typescript-eslint/no-inferrable-types */

const SEED_${pad}: number = ${index * 7 + 13};

class Registry_${pad} {
	private readonly store: Map<string, number> = new Map();
	private counter: number = SEED_${pad};

	public register(key: string): number {
		let hash: number = this.counter;
		for (let i: number = 0; i < key.length; i++) {
			hash = ((hash << 5) - hash + key.charCodeAt(i)) | 0;
			hash = (hash ^ (hash >>> 16)) | 0;
		}
		this.store.set(key, hash);
		this.counter = (this.counter + hash) | 0;
		return hash;
	}

	public lookup(key: string): number {
		return this.store.get(key) ?? -1;
	}

	public transform(): number[] {
		const values: number[] = [];
		for (const [, v] of this.store) {
			values.push(((v * 0x45d9f3b) ^ (v >>> 16)) | 0);
		}
		return values.sort((a, b) => a - b);
	}

	public aggregate(): number {
		let acc: number = 0;
		for (const [, v] of this.store) {
			acc = (acc + ((v >>> 0) % 1000003)) | 0;
		}
		return acc;
	}
}

class Processor_${pad} {
	private readonly pipeline: Array<(n: number) => number> = [];

	public constructor() {
		this.pipeline.push(
			(n) => ((n * 2654435761) >>> 0) | 0,
			(n) => ((n ^ (n >>> 13)) * 0x5bd1e995) | 0,
			(n) => (n ^ (n >>> 15)) | 0,
			(n) => ((n + 0x9e3779b9) ^ (n << 6) ^ (n >>> 2)) | 0,
		);
	}

	public run(input: number): number {
		let value: number = input;
		for (const fn of this.pipeline) {
			value = fn(value);
		}
		return value;
	}

	public batch(inputs: number[]): number[] {
		return inputs.map((n) => this.run(n));
	}
}

class Encoder_${pad} {
	private readonly table: string[] = [];

	public constructor(size: number) {
		for (let i: number = 0; i < size; i++) {
			this.table.push(String.fromCharCode(65 + (i % 26)).repeat((i % 4) + 1));
		}
	}

	public encode(value: number): string {
		const idx: number = ((value >>> 0) % this.table.length) | 0;
		return this.table[idx] ?? "";
	}

	public encodeAll(values: number[]): string[] {
		return values.map((v) => this.encode(v));
	}
}

/**
 * Entry point for chunk ${pad}. Performs work to prevent tree-shaking.
 */
export function runChunk_${pad}(): number {
	const registry = new Registry_${pad}();
	const processor = new Processor_${pad}();
	const encoder = new Encoder_${pad}(64);

	const keys: string[] = [
		"alpha_${pad}", "beta_${pad}", "gamma_${pad}",
		"delta_${pad}", "epsilon_${pad}", "zeta_${pad}",
		"eta_${pad}", "theta_${pad}", "iota_${pad}",
	];

	for (const key of keys) {
		registry.register(key);
	}

	const transformed = registry.transform();
	const processed = processor.batch(transformed);
	const encoded = encoder.encodeAll(processed);
	const aggregate = registry.aggregate();

	return aggregate + encoded.length + processed.reduce((a, b) => (a + b) | 0, 0);
}
`;
}

/**
 * Generate a barrel file that imports and calls all chunk entry points.
 */
function generateBarrel(count: number): string {
	const imports: string[] = [];
	const calls: string[] = [];

	for (let i = 0; i < count; i++) {
		const pad = String(i).padStart(4, "0");
		imports.push(`import { runChunk_${pad} } from "./chunk-${pad}.js";`);
		calls.push(`\tresult = (result + runChunk_${pad}()) | 0;`);
	}

	return `/*!
 * Copyright (c) Microsoft Corporation and contributors. All rights reserved.
 * Licensed under the MIT License.
 */

// Auto-generated by scripts/generateBallast.ts — do not edit.
// This barrel is self-executing: webpack includes it as an additional entry
// so the code's parse/compile/execute cost is reflected in Lighthouse metrics.

${imports.join("\n")}

function runAllChunks(): number {
\tlet result: number = 0;
${calls.join("\n")}
\treturn result;
}

// Execute so the ballast is not tree-shaken.
console.log("ballast", runAllChunks());
`;
}

// Main
console.log(`Generating ${chunkCount} ballast chunks in ${outDir}/`);

// Clean and recreate
rmSync(outDir, { recursive: true, force: true });
mkdirSync(outDir, { recursive: true });

for (let i = 0; i < chunkCount; i++) {
	const pad = String(i).padStart(4, "0");
	writeFileSync(join(outDir, `chunk-${pad}.ts`), generateChunk(i));
}

writeFileSync(join(outDir, "index.ts"), generateBarrel(chunkCount));

console.log(`Done. Generated ${chunkCount} chunks + barrel index.ts`);
