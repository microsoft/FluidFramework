import type { ImageMediaType } from "./llmTypes.js";

/**
 * Pre-encoded base64 image data.
 * @legacy
 * @alpha
 */
export interface ImageBase64 {
	type: "base64";
	mediaType: ImageMediaType;
	data: string;
}

/**
 * Image input for evaluation.
 * - A bare `string` is treated as a file path (read from disk at eval time).
 * - An `ImageBase64` object provides pre-encoded image data.
 * @legacy
 * @alpha
 */
export type ImageInput = string | ImageBase64;

/**
 * Eval configuration for a scenario (rubrics, prompts, scale).
 * @legacy
 * @alpha
 */
export interface LlmEvalConfig {
	rubrics: Rubric[];
	dataInterpretationPrompt?: string;
	/** Default scoring scale for all rubrics (default: \{ min: 0, max: 5 \}). */
	defaultScale?: ScoreScale;
	/**
	 * Additional free-text fields to request from the LLM beyond per-rubric scores.
	 * Each entry defines a field name and a description of what the LLM should provide.
	 * The LLM will be prompted to include these fields after the rubric scores,
	 * and their values will appear in `EvaluationResult.additionalFields`.
	 *
	 * Example:
	 * ```
	 * additionalFields: [
	 *   { name: 'overall_impression', description: '1-2 sentence overall assessment' },
	 *   { name: 'improvement_recommendations', description: 'Array of 2+ actionable recommendations as a JSON array' },
	 * ]
	 * ```
	 */
	additionalFields?: AdditionalFieldConfig[];
	/**
	 * Custom thresholds for status classification (default: good=80, pass=60).
	 * Useful when different scoring contexts need different pass criteria
	 * (e.g., rubric pass/fail might require 80% instead of 60%).
	 */
	statusThresholds?: StatusThresholds;
}

/**
 * Configuration for an additional free-text field requested from the LLM.
 * @legacy
 * @alpha
 */
export interface AdditionalFieldConfig {
	/** Field name (used as key in the result). */
	name: string;
	/** Description of what the LLM should provide for this field. */
	description: string;
}

/**
 * Custom thresholds for status classification.
 * Percentages are compared against overallPercentage.
 * @legacy
 * @alpha
 */
export interface StatusThresholds {
	/** Minimum percentage for 'GOOD' status (default: 80). */
	good?: number;
	/** Minimum percentage for 'PASS' status (default: 60). */
	pass?: number;
}

/**
 * Per-dataset artifacts produced by the generation phase.
 * All data is provided in-memory, except for image file paths specified in DatasetArtifact.
 * @legacy
 * @alpha
 */
export interface DatasetArtifact {
	/** Dataset name — links back to the corresponding Dataset entry */
	name: string;
	/**
	 * Application input that is used to generate the application output (e.g., the user prompt, context).
	 * This can be useful for evaluation depending on the application. For example, if the application is a
	 * question-answering system, the input would be the question and the output would be the answer.
	 */
	input?: JsonObject;
	/**
	 * Application output that is generated from the input (e.g., the application final state, an Agent response).
	 * This is the primary artifact that will be evaluated by the LLM judge. For example, if the application is a
	 * chat, the output would be the response generated by an Agent.
	 */
	output: JsonObject;
	/**
	 * Images to be used for evaluation (e.g., screenshots, diagrams).
	 * File path images must be under 5 MB each and use a supported extension
	 * (.png, .jpg, .jpeg, .gif, .webp). Unsupported formats are rejected at eval time.
	 * This can be useful for evaluating multimodal applications or providing visual context to the LLM judge.
	 * For example, if the application is a design tool, the images could be screenshots of the generated designs
	 * to evaluate visual quality.
	 */
	images?: ImageInput[];
	/** Opaque metadata from the generator, passed through to results */
	metadata: JsonObject;
}

/**
 * All generated artifacts for a scenario.
 * All data is provided in-memory — the framework does not read files.
 * @legacy
 * @alpha
 */
export interface ScenarioArtifact {
	/** Name of the scenario */
	name: string;
	/** Eval configuration: rubrics, prompts, scoring scale. */
	llmEvalConfig: LlmEvalConfig;
	/** One entry per dataset in the scenario */
	datasetArtifacts: DatasetArtifact[];
	/** Model type used for generation */
	modelType: string;
	/** Opaque metadata from the generator, passed through to results */
	metadata: JsonObject;
	/**
	 * Optional scenario-level properties that are written to result.json and summary.md,
	 * and surfaced in the eval app as additional columns and filters.
	 */
	customResultProperties?: Record<string, CustomPropertyValueType>;
}

/**
 * Scoring scale range for a rubric dimension.
 * @legacy
 * @alpha
 */
export interface ScoreScale {
	/** Minimum score (default: 0) */
	min: number;
	/** Maximum score (default: 5) */
	max: number;
}

/**
 * Default scoring scale used when no scale is specified.
 * @legacy
 * @alpha
 */
export const DEFAULT_SCALE: ScoreScale = Object.freeze({ min: 0, max: 5 });

/**
 * A scoring rubric dimension for LLM-as-judge evaluation.
 * Each rubric defines a dimension name and what it measures.
 * @legacy
 * @alpha
 */
export interface Rubric {
	/** Display name of the dimension (e.g., "Task Completion") */
	name: string;
	/** Description of what this dimension evaluates — should be specific and measurable */
	description: string;
	/** If true, the LLM may respond "N/A" and the score will be null */
	optional?: boolean;
}

/**
 * Allowed value types for custom scenario properties that appear in results and UI.
 * @legacy
 * @alpha
 */
export type CustomPropertyValueType = boolean | string | number;

/**
 * A JSON-serializable object
 * @legacy
 * @alpha
 */
export type JsonObject = Record<string, unknown>;
